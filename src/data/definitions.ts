export function getDefinitionById(id: string): Definition | undefined {
  return definitions.find(def => def.id === id);
}

export function getDefinitionsByCategory(category: Definition['category']): Definition[] {
  return definitions.filter(def => def.category === category);
}

export function searchDefinitions(query: string): Definition[] {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return definitions;
  
  return definitions.filter(def => 
    def.term.toLowerCase().includes(normalizedQuery) ||
    def.shortDefinition.toLowerCase().includes(normalizedQuery) ||
    def.fullDefinition.toLowerCase().includes(normalizedQuery) ||
    def.relatedTerms.some((term: string) => term.toLowerCase().includes(normalizedQuery))
  );
}

export function getRelatedDefinitions(termId: string): Definition[] {
  const definition = getDefinitionById(termId);
  if (!definition) return [];
  
  return definitions.filter(def => 
    definition.relatedTerms.includes(def.id) || 
    def.relatedTerms.includes(termId)
  );
}